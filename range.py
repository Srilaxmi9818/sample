#print all even numbers from 1 to 20 using range()
print("All even numbers from 1 to 20")
# Iterate over the range of numbers from 1 to 21 (exclusive) with step size 2 to get even numbers
for num in range(2, 21, 2):
    print(num)


print ("First five multiples of 3")
# Iterate over the range of numbers from 1 to 6 (exclusive) to get the first 5 multiples of 3
for i in range(1, 6):
    print(i * 3)

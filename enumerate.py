#print index annd value using enumerate function
# Define a list
my_list = ['apple', 'banana', 'cherry', 'date']

# Iterate over the list using enumerate()
for index, value in enumerate(my_list):
    print("Index:", index, "Value:", value)


#print keys and values in dictionary using enumerate function
# Define a dictionary
my_dict = {'apple': 3, 'banana': 5, 'cherry': 8}

# Iterate over the dictionary and print key-value pairs
for key, value in my_dict.items():
    print("Key:", key, "Value:", value)
